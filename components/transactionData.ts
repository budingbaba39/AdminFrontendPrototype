export interface Transaction {
  id: string;
  type: string;
  username: string;
  name: string;
  mobile: string;
  bankAccountName: string;
  bankAccountNumber: string;
  method: string;
  from: string;
  depositFee?: number;
  withdrawalFee?: number;
  amount: number;
  status: 'PENDING' | 'PROCESSING' | 'APPROVED' | 'COMPLETED' | 'REJECTED';
  submitTime: string;
  processTime?: string;
  completeTime?: string;
  rejectTime?: string;
  processBy?: string;
  completeBy?: string;
  rejectBy?: string;
  currentCredit: number;
  remark: string;
  receiptImage?: string;
  hasAttachment?: boolean;
  // New fields
  rebateName?: string;
  commissionType?: string;
  commissionName?: string;
  promotionName?: string;
  promotionID?: string;
  promotionType?:string;
  lossAmount?: number; // User's actual loss from betting (only for REBATE type)
  cashbackName?: string;
  cashbackType?: string;
  // Promotion tracking fields
  turnoverCurrent?: number;  // Current turnover achieved
  turnoverTarget?: number;   // Required turnover to complete
  transferAmount?: number;   // Deposit amount for promotion
  bonusAmount?: number;      // Calculated bonus amount
  startTime?: string;        // When promotion was approved (started)
}

