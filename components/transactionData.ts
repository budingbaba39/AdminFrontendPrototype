export interface Transaction {
  id: string;
  type: string;
  userID: string;
  name?: string;
  mobile: string;
  bankAccountName: string;
  bankAccountNumber: string;
  method: string;
  from: string;
  depositFee?: number;
  withdrawalFee?: number;
  amount: number;
  status: 'PENDING' | 'PROCESSING' | 'APPROVED' | 'COMPLETED' | 'REJECTED';
  submitTime: string;
  processTime?: string;
  completeTime?: string;
  rejectTime?: string;
  processBy?: string;
  completeBy?: string;
  rejectBy?: string;
  currentCredit: number;
  remark: string;
  receiptImage?: string;
  hasAttachment?: boolean;
  // New fields
  rebateName?: string;
  rebateType?: string;
  rebateAmount?: number;
  rebateTargetType?: string;
  rebatePercentage?: number;
  rebateMaxWithdrawalAmount?: number;
  rebateSetupId?: string;
  rebateCurrent?: number;
  rebateTarget?: number;
  commissionType?: string;
  commissionName?: string;
  promotionName?: string;
  promotionID?: string;
  promotionType?:string;
  lossAmount?: number; // User's actual loss from betting (only for REBATE type)
  cashbackName?: string;
  cashbackType?: string;
  cashbackTargetType?: string;
  cashbackPercentage?: number;
  cashbackMaxWithdrawalAmount?: number;
  cashbackSetupId?: string;
  cashbackCurrent?: number;
  cashbackTarget?: number;
  cashbackAmount?: number;
  // Promotion tracking fields
  turnoverCurrent?: number;  // Current turnover achieved
  turnoverTarget?: number;   // Required turnover to complete
  transferAmount?: number;   // Deposit amount for promotion
  bonusAmount?: number;      // Calculated bonus amount
  startTime?: string;        // When promotion was approved (started)
  // Commission tracking fields
  commissionTargetType?: 'Deposit - Withdraw' | 'Deposit - Withdraw - Rebate - Bonus' | 'Valid Bet';
  commissionPercentage?: number;
  commissionMaxWithdrawalAmount?: number;
  commissionSetupId?: string; // Links to CommissionSetup
  commissionCurrent?: number;  // Current commission progress achieved
  commissionTarget?: number;   // Required commission target to complete
  commissionAmount?: number;   // Calculated commission amount earned
  depositAmount?: number;      // For Deposit-Withdraw type commissions
  withdrawAmount?: number;     // For Deposit-Withdraw type commissions
  validBetAmount?: number;     // For Valid Bet type commissions
  // Approval tracking fields (for backend logic)
  is_auto_approved?: boolean;
  approved_by?: string;
  approved_at?: string;
}

